#include <stdio.h>

/**
 * add - adds two int
 * @j: first int
 * @k: second in
 * Return: addition result
 */
int add(int j, int k)
{
	return (j + k);
}

/**
 * sub - subtracts two int
 * @j: first int
 * @k: second in
 * Return: subtracted result
 */
int sub(int j, int k)
{
	return (j - k);
}

/**
 * mod - mod two int
 * @j: first int
 * @k: second in
 * Return: modulus result
 */
int mod(int j, int k)
{
	return (j % k);
}

/**
 * mul - multiplies two int
 * @j: first int
 * @k: second in
 * Return: multiplication result
 */
int mul(int j, int k)
{
	return (j % k);
}

/**
 * div - divides two int
 * @j: first int
 * @k: second in
 * Return: division result
 */
int div(int j, int k)
{
	return (j / k);
}
